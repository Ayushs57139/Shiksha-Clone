export const getAllAgricultureInstitutions = () => {
  return nirfAgricultureData;
};

export const getAgricultureInstitutionsByRankRange = (startRank, endRank) => {
  return nirfAgricultureData.filter(institution => 
    institution.nirfRank >= startRank && institution.nirfRank <= endRank
  );
};

export const getTop10AgricultureInstitutions = () => {
  return nirfAgricultureData.filter(institution => institution.nirfRank <= 10);
};

export const getAgricultureInstitutionsByState = (state) => {
  return nirfAgricultureData.filter(institution => 
    institution.state.toLowerCase() === state.toLowerCase()
  );
};

export const getICARInstitutions = () => {
  return nirfAgricultureData.filter(institution => 
    institution.highlights.some(highlight => highlight.includes('ICAR'))
  );
};

export const getStateAgricultureUniversities = () => {
  return nirfAgricultureData.filter(institution => 
    institution.highlights.some(highlight => highlight.includes('State University'))
  );
}; 