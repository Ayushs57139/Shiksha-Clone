export const getAllOpenUniversityInstitutions = () => {
  return nirfOpenUniversityData;
};

export const getOpenUniversityInstitutionsByRankRange = (startRank, endRank) => {
  return nirfOpenUniversityData.filter(institution => 
    institution.nirfRank >= startRank && institution.nirfRank <= endRank
  );
};

export const getTop10OpenUniversityInstitutions = () => {
  return nirfOpenUniversityData.filter(institution => institution.nirfRank <= 10);
};

export const getOpenUniversityInstitutionsByState = (state) => {
  return nirfOpenUniversityData.filter(institution => 
    institution.state.toLowerCase() === state.toLowerCase()
  );
};

export const getIGNOUInstitutions = () => {
  return nirfOpenUniversityData.filter(institution => 
    institution.highlights.some(highlight => highlight.includes('IGNOU'))
  );
};

export const getStateOpenUniversities = () => {
  return nirfOpenUniversityData.filter(institution => 
    institution.highlights.some(highlight => highlight.includes('State'))
  );
}; 