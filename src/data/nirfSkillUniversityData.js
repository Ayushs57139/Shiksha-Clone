export const getTop10SkillUniversities = () => {
  return nirfSkillUniversityData.slice(0, 10);
};

export const getSkillUniversitiesByState = (state) => {
  return nirfSkillUniversityData.filter(university => 
    university.state.toLowerCase().includes(state.toLowerCase())
  );
};

export const getSkillUniversitiesByCity = (city) => {
  return nirfSkillUniversityData.filter(university => 
    university.city.toLowerCase().includes(city.toLowerCase())
  );
};

export const getSkillUniversitiesByType = (type) => {
  return nirfSkillUniversityData.filter(university => 
    university.type.toLowerCase() === type.toLowerCase()
  );
}; 